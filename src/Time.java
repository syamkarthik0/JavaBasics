public class Time {
    public static void main(String args[]){
        for(int i=0;i<24;i++){
            for(int j=1;j<=60;j++){
                for(int k=1;k<=60;k++){
                    System.out.println(i+" hours "+j+" Minutes "+k+" Seconds");
                }
            }
        }
    }
    
}
